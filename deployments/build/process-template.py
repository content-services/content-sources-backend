#!/usr/bin/env python3
"""
Template processor for content-sources-backend deployment.yaml.

Reads deployment.template.yaml and env-variables.yaml (single 'env' list).
Replaces every {{ENV_...}} placeholder with the same full env list. Writes deployment.yaml.
"""

import os
import sys
from typing import Any, Dict, List

import yaml


def load_yaml_file(file_path: str) -> Dict[str, Any]:
    """Load a YAML file and return its contents."""
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            return yaml.safe_load(f)
    except FileNotFoundError:
        print(f"Error: File {file_path} not found", file=sys.stderr)
        sys.exit(1)
    except yaml.YAMLError as e:
        print(f"Error parsing YAML file {file_path}: {e}", file=sys.stderr)
        sys.exit(1)


def format_env_vars(env_vars: List[Dict[str, Any]]) -> str:
    """Format a list of env entries as YAML for insertion under 'env:' (14 spaces before '-')."""
    if not env_vars:
        return ""
    yaml_str = yaml.dump(env_vars, default_flow_style=False, sort_keys=False, allow_unicode=True)
    lines = yaml_str.strip().split("\n")
    indented = []
    for line in lines:
        indented.append("              " + line)
    return "\n".join(indented).rstrip()


def process_template(template_content: str, env_data: Dict[str, Any]) -> str:
    """Replace each {{ENV_...}} placeholder with the full env list."""
    env_list = env_data.get("env") or []
    formatted = "\n" + format_env_vars(env_list) if env_list else ""
    placeholders = (
        "ENV_VARS",
    )
    result = template_content
    for key in placeholders:
        result = result.replace("{{" + key + "}}", formatted)
    return result


def main() -> None:
    script_dir = os.path.dirname(os.path.abspath(__file__))
    template_path = os.path.join(script_dir, "deployment.template.yaml")
    env_path = os.path.join(script_dir, "env-variables.yaml")
    out_path = os.path.join(os.path.dirname(script_dir), "deployment.yaml")

    if not os.path.exists(template_path):
        print(f"Error: Template file {template_path} not found", file=sys.stderr)
        sys.exit(1)
    if not os.path.exists(env_path):
        print(f"Error: Environment variables file {env_path} not found", file=sys.stderr)
        sys.exit(1)

    with open(template_path, "r", encoding="utf-8") as f:
        template_content = f.read()
    env_data = load_yaml_file(env_path)

    processed = process_template(template_content, env_data)

    header = (
        "# DO NOT EDIT THIS FILE DIRECTLY.\n"
        "# This file is generated from deployment.template.yaml and env-variables.yaml\n"
        "# in deployments/build/. Run 'make deployment-generate' to regenerate.\n\n"
    )
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(header)
        f.write(processed)
    print(f"Generated {out_path}")


if __name__ == "__main__":
    main()
